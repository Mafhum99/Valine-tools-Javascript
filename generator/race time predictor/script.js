/**
 * Race Time Predictor
 * Predict race times for different distances using Riegel's formula
 * T2 = T1 × (D2 / D1)^1.06
 */
document.addEventListener('DOMContentLoaded', () => {
    initTool({ name: 'Race Time Predictor', icon: '🏁' });

    const raceDistEl = $('#raceDistance');
    const customDistGroup = $('#customDistGroup');
    const customDistEl = $('#customDist');
    const hoursEl = $('#hours');
    const minutesEl = $('#minutes');
    const secondsEl = $('#seconds');
    const outputEl = $('#output');
    const predictBtn = $('#predict');
    const clearBtn = $('#clear');
    const copyBtn = $('#copy');

    raceDistEl.addEventListener('change', () => {
        customDistGroup.classList.toggle('hidden', raceDistEl.value !== 'custom');
    });

    function formatTime(totalSeconds) {
        const h = Math.floor(totalSeconds / 3600);
        const m = Math.floor((totalSeconds % 3600) / 60);
        const s = Math.round(totalSeconds % 60);
        if (h > 0) return `${h}:${m.toString().padStart(2, '0')}:${s.toString().padStart(2, '0')}`;
        return `${m}:${s.toString().padStart(2, '0')}`;
    }

    function predict() {
        const h = parseInt(hoursEl.value) || 0;
        const m = parseInt(minutesEl.value) || 0;
        const s = parseInt(secondsEl.value) || 0;
        const totalSecs = h * 3600 + m * 60 + s;

        if (totalSecs <= 0) { outputEl.textContent = 'Error: Enter a valid time > 0.'; return; }

        let knownDist;
        if (raceDistEl.value === 'custom') {
            knownDist = parseFloat(customDistEl.value.trim());
            if (isNaN(knownDist) || knownDist <= 0) { outputEl.textContent = 'Error: Enter a valid custom distance > 0.'; return; }
        } else {
            knownDist = parseFloat(raceDistEl.value);
        }

        // Riegel's formula: T2 = T1 × (D2 / D1)^1.06
        function predictTime(targetDist) {
            return totalSecs * Math.pow(targetDist / knownDist, 1.06);
        }

        const races = [
            { name: '1K', dist: 1 },
            { name: 'Mile', dist: 1.609344 },
            { name: '5K', dist: 5 },
            { name: '10K', dist: 10 },
            { name: '15K', dist: 15 },
            { name: '10 Mile', dist: 16.09344 },
            { name: 'Half Marathon', dist: 21.0975 },
            { name: '25K', dist: 25 },
            { name: '30K', dist: 30 },
            { name: 'Marathon', dist: 42.195 },
            { name: '50K', dist: 50 },
            { name: '50 Mile', dist: 80.4672 }
        ];

        let result = `Race Time Predictions (Riegel's Formula)\n`;
        result += `Based on: ${formatTime(totalSecs)} for ${formatNumber(knownDist, 2)} km\n`;
        result += `━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n\n`;

        const pacePerKm = totalSecs / knownDist;
        result += `Current pace: ${formatTime(pacePerKm)} /km\n`;
        result += `Speed: ${formatNumber((knownDist / totalSecs) * 3600, 2)} km/h\n\n`;

        result += `Predicted Times:\n`;
        races.forEach(race => {
            if (Math.abs(race.dist - knownDist) > 0.001) {
                const predicted = predictTime(race.dist);
                result += `${race.name.padEnd(16)} ${formatTime(predicted)}\n`;
            }
        });

        result += `\nFormula: T2 = T1 × (D2/D1)^1.06\n`;
        result += `Note: Predictions are estimates. Actual\n`;
        result += `performance varies by fitness and training.`;

        outputEl.textContent = result;
    }

    predictBtn.addEventListener('click', predict);
    clearBtn.addEventListener('click', () => {
        hoursEl.value = '0'; minutesEl.value = '0'; secondsEl.value = '0'; customDistEl.value = '';
        outputEl.textContent = '-'; hoursEl.focus();
    });
    copyBtn.addEventListener('click', () => copyToClipboard(outputEl.textContent));
    [hoursEl, minutesEl, secondsEl, customDistEl].forEach(el => el.addEventListener('keypress', (e) => { if (e.key === 'Enter') predict(); }));
});
