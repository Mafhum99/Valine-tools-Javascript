/**
 * Experiment Hypothesis Generator
 * Generate experiment hypotheses from test descriptions
 */

document.addEventListener('DOMContentLoaded', () => {
    initTool({ name: 'Experiment Hypothesis Generator', icon: '🔬' });

    const inputEl = $('#input');
    const outputEl = $('#output');
    const calculateBtn = $('#calculate');
    const clearBtn = $('#clear');
    const copyBtn = $('#copy');

    function generateHypotheses(experiment) {
        const expTitle = titleCase(experiment);
        let output = `EXPERIMENT HYPOTHESES: ${expTitle.toUpperCase()}\n`;
        output += `${'='.repeat(55)}\n\n`;

        const hypothesisTemplates = [
            {
                angle: 'User Experience',
                if: `we improve the ${expTitle.toLowerCase()} experience`,
                then: `users will complete the task faster and with fewer errors`,
                because: `a streamlined experience reduces cognitive load and friction points`,
                metric: 'Task completion time and error rate',
                expectedImpact: '10-20% improvement'
            },
            {
                angle: 'Conversion Optimization',
                if: `we optimize the ${expTitle.toLowerCase()} flow`,
                then: `more visitors will convert to customers`,
                because: `reducing friction in the flow removes barriers to conversion`,
                metric: 'Conversion rate',
                expectedImpact: '5-15% lift'
            },
            {
                angle: 'Engagement',
                if: `we make ${expTitle.toLowerCase()} more interactive`,
                then: `users will spend more time engaging with the feature`,
                because: `interactive elements increase user investment and attention`,
                metric: 'Average session duration and feature adoption rate',
                expectedImpact: '15-25% increase in engagement'
            },
            {
                angle: 'Trust and Credibility',
                if: `we add social proof to ${expTitle.toLowerCase()}`,
                then: `new users will be more likely to sign up or purchase`,
                because: `social proof reduces perceived risk and builds confidence`,
                metric: 'Sign-up rate and purchase completion',
                expectedImpact: '8-12% improvement'
            },
            {
                angle: 'Mobile Optimization',
                if: `we redesign ${expTitle.toLowerCase()} for mobile-first`,
                then: `mobile users will show engagement parity with desktop users`,
                because: `mobile-optimized design addresses the unique constraints of smaller screens`,
                metric: 'Mobile conversion rate and bounce rate',
                expectedImpact: '20-30% reduction in mobile bounce rate'
            },
            {
                angle: 'Personalization',
                if: `we personalize ${expTitle.toLowerCase()} based on user behavior`,
                then: `users will find relevant content more quickly`,
                because: `personalized experiences match individual preferences and intent`,
                metric: 'Click-through rate and user satisfaction score',
                expectedImpact: '10-18% increase in CTR'
            }
        ];

        hypothesisTemplates.forEach((h, i) => {
            output += `HYPOTHESIS #${i + 1}: ${h.angle}\n`;
            output += `${'-'.repeat(45)}\n`;
            output += `IF ${h.if},\n`;
            output += `THEN ${h.then},\n`;
            output += `BECAUSE ${h.because}.\n\n`;
            output += `Primary Metric: ${h.metric}\n`;
            output += `Expected Impact: ${h.expectedImpact}\n`;
            output += `Confidence Level: ${i < 2 ? 'High' : i < 4 ? 'Medium' : 'Low'}\n`;
            output += `Priority: ${i < 2 ? 'P0 - Test first' : i < 4 ? 'P1 - Test next' : 'P2 - Test later'}\n`;
            output += `${'='.repeat(55)}\n\n`;
        });

        output += `EXPERIMENT DESIGN NOTES\n`;
        output += `${'-'.repeat(30)}\n`;
        output += `1. Define clear success criteria before starting\n`;
        output += `2. Ensure statistical significance (minimum 95% confidence)\n`;
        output += `3. Run experiments for at least 2 full business cycles\n`;
        output += `4. Monitor for novelty effects and seasonality\n`;
        output += `5. Document learnings regardless of outcome\n`;
        output += `6. Plan follow-up experiments based on results\n`;

        return output;
    }

    function calculate() {
        const input = inputEl.value.trim();
        if (!input) {
            outputEl.textContent = 'Please enter an experiment or change to test';
            return;
        }
        try {
            outputEl.textContent = generateHypotheses(input);
        } catch (error) {
            outputEl.textContent = 'Error: ' + error.message;
        }
    }

    function clear() {
        inputEl.value = '';
        outputEl.textContent = '-';
        inputEl.focus();
    }

    calculateBtn.addEventListener('click', calculate);
    clearBtn.addEventListener('click', clear);
    if (copyBtn) copyBtn.addEventListener('click', () => copyToClipboard(outputEl.textContent));
    inputEl.addEventListener('keypress', (e) => { if (e.key === 'Enter') calculate(); });
});
