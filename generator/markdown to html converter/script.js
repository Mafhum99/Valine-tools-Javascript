/**
 * Markdown to HTML Converter
 * Convert MD headings, bold, italic, lists, links, code to HTML
 */

document.addEventListener('DOMContentLoaded', () => {
    initTool({ name: 'Markdown to HTML Converter', icon: '\u{1F4DD}' });

    const inputEl = $('#input');
    const outputEl = $('#output');
    const convertBtn = $('#convertBtn');
    const clearBtn = $('#clearBtn');
    const copyBtn = $('#copyBtn');

    function markdownToHtml(md) {
        let html = md;

        // Escape HTML entities first (to prevent double-encoding)
        html = html.replace(/&/g, '&amp;');
        html = html.replace(/</g, '&lt;');
        html = html.replace(/>/g, '&gt;');

        // Code blocks (``` ... ```)
        html = html.replace(/```(\w*)\n([\s\S]*?)```/g, '<pre><code class="language-$1">$2</code></pre>');

        // Inline code
        html = html.replace(/`([^`]+)`/g, '<code>$1</code>');

        // Headings
        html = html.replace(/^######\s+(.+)$/gm, '<h6>$1</h6>');
        html = html.replace(/^#####\s+(.+)$/gm, '<h5>$1</h5>');
        html = html.replace(/^####\s+(.+)$/gm, '<h4>$1</h4>');
        html = html.replace(/^###\s+(.+)$/gm, '<h3>$1</h3>');
        html = html.replace(/^##\s+(.+)$/gm, '<h2>$1</h2>');
        html = html.replace(/^#\s+(.+)$/gm, '<h1>$1</h1>');

        // Horizontal rule
        html = html.replace(/^(?:---|\*\*\*|___)\s*$/gm, '<hr>');

        // Bold and italic (order matters)
        html = html.replace(/\*\*\*(.+?)\*\*\*/g, '<strong><em>$1</em></strong>');
        html = html.replace(/___(.+?)___/g, '<strong><em>$1</em></strong>');

        // Bold
        html = html.replace(/\*\*(.+?)\*\*/g, '<strong>$1</strong>');
        html = html.replace(/__(.+?)__/g, '<strong>$1</strong>');

        // Italic
        html = html.replace(/\*(.+?)\*/g, '<em>$1</em>');
        html = html.replace(/_(.+?)_/g, '<em>$1</em>');

        // Strikethrough
        html = html.replace(/~~(.+?)~~/g, '<del>$1</del>');

        // Images
        html = html.replace(/!\[([^\]]*)\]\(([^)]+)\)/g, '<img src="$2" alt="$1">');

        // Links
        html = html.replace(/\[([^\]]+)\]\(([^)]+)\)/g, '<a href="$2">$1</a>');

        // Unordered lists
        html = html.replace(/^[\s]*[-*+]\s+(.+)$/gm, '<li>$1</li>');
        html = html.replace(/((?:<li>.*<\/li>\n?)+)/g, '<ul>$1</ul>');

        // Ordered lists
        html = html.replace(/^[\s]*\d+\.\s+(.+)$/gm, '<oli>$1</oli>');
        html = html.replace(/((?:<oli>.*<\/oli>\n?)+)/g, (match) => {
            return '<ol>' + match.replace(/<\/?oli>/g, (tag) => tag.replace('oli', 'li')) + '</ol>';
        });

        // Blockquotes
        html = html.replace(/^&gt;\s+(.+)$/gm, '<blockquote>$1</blockquote>');

        // Paragraphs: wrap lines that are not already in HTML tags
        const lines = html.split('\n');
        const processed = [];
        let inBlock = false;
        let blockContent = [];

        for (let i = 0; i < lines.length; i++) {
            const line = lines[i].trim();

            if (line === '') {
                if (blockContent.length > 0) {
                    processed.push('<p>' + blockContent.join('<br>') + '</p>');
                    blockContent = [];
                }
                continue;
            }

            // Check if line is already an HTML block element
            if (/^<(h[1-6]|ul|ol|li|pre|blockquote|hr|div|table|thead|tbody|tr|th|td)/.test(line) ||
                /<\/(h[1-6]|ul|ol|pre|blockquote|div|table|thead|tbody)>$/.test(line)) {
                if (blockContent.length > 0) {
                    processed.push('<p>' + blockContent.join('<br>') + '</p>');
                    blockContent = [];
                }
                processed.push(line);
            } else {
                blockContent.push(line);
            }
        }

        // Flush remaining
        if (blockContent.length > 0) {
            processed.push('<p>' + blockContent.join('<br>') + '</p>');
        }

        return processed.join('\n');
    }

    function execute() {
        const input = inputEl.value.trim();
        if (!input) {
            outputEl.textContent = 'Please enter Markdown to convert';
            return;
        }
        try {
            outputEl.textContent = markdownToHtml(input);
        } catch (e) {
            outputEl.textContent = '\u274C Error: ' + e.message;
        }
    }

    function clear() {
        inputEl.value = '';
        outputEl.textContent = '-';
        inputEl.focus();
    }

    convertBtn.addEventListener('click', execute);
    clearBtn.addEventListener('click', clear);

    if (copyBtn) {
        copyBtn.addEventListener('click', () => copyToClipboard(outputEl.textContent));
    }
});
